import streamlit as st

st.title("🎬 오늘 기분에 맞는 영화 추천")

# 1. 사용자 상황 입력
mood = st.radio("오늘 기분이 어떠신가요?", ["우울해서 힐링이 필요해", "심심해서 짜릿한 게 좋아", "잔잔하게 감성을 채우고 싶어"])
time_available = st.slider("가용 시간(분)", 30, 180, 120)

# 2. 추천 로직 (데이터베이스 대신 딕셔너리 활용)
recommendations = {
    "우울해서 힐링이 필요해": {"제목": "인턴", "이유": "따뜻한 조언이 필요할 때 최고예요.", "간식": "따뜻한 코코아"},
    "심심해서 짜릿한 게 좋아": {"제목": "인셉션", "이유": "시간 가는 줄 모르는 몰입감을 줍니다.", "간식": "팝콘과 콜라"},
    "잔잔하게 감성을 채우고 싶어": {"제목": "어바웃 타임", "이유": "일상의 소중함을 느끼게 해줍니다.", "간식": "차 한 잔"}
}

# 3. 결과 출력
if st.button("추천 영화 보기"):
    result = recommendations[mood]
    st.success(f"🎥 추천 영화: **{result['제목']}**")
    st.write(f"💬 이유: {result['이유']}")
    st.info(f"🍿 같이 먹으면 좋은 간식: {result['간식']}")
